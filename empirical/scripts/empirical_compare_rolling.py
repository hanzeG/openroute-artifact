def _mutable_tiers_to_segments(
    mtiers: List[Dict[str, Any]],
    in_cur: str,
    out_cur: str,
    src: str = "CLOB",
) -> List[Segment]:
    segs: List[Segment] = []
    for t in mtiers:
        out_amt = t.get("out_amt")
        in_amt = t.get("in_amt")
        if out_amt is None or in_amt is None:
            # Backward compatibility for older state payloads.
            out_dec = D(t.get("out_dec"))
            in_dec = D(t.get("in_dec"))
            if out_dec <= 0 or in_dec <= 0:
                continue
            out_amt = amt_floor(out_cur, out_dec)
            in_amt = amt_floor(in_cur, in_dec)
        if amount_to_decimal(out_amt) <= 0 or amount_to_decimal(in_amt) <= 0:
            continue
        q = Quality.from_amounts(out_amt, in_amt)
        segs.append(
            Segment(
                src=src,
                quality=q,
                out_max=out_amt,
                in_at_out_max=in_amt,
                raw_quality=q,
                source_id=str(t.get("source_id")),
            )
        )
    return segs


def _consume_clob_slices_inplace(
    mtiers: List[Dict[str, Any]],
    quote: Dict[str, Any],
    in_cur: str,
    out_cur: str,
) -> None:
    by_id = {str(t.get("source_id")): t for t in mtiers}
    for s in quote.get("slices", []) or []:
        if not isinstance(s, dict):
            continue
        src = str(s.get("src") or s.get("kind") or "")
        if normalise_src(src) != "CLOB":
            continue
        sid = str(s.get("source_id") or "")
        if not sid or sid not in by_id:
            continue
        out_take = s.get("out_take")
        if out_take is None:
            continue
        out_take_u = amt_units_floor(out_take)
        if out_take_u <= 0:
            continue
        t = by_id[sid]
        cur_out_amt = t.get("out_amt")
        cur_in_amt = t.get("in_amt")
        if cur_out_amt is None or cur_in_amt is None:
            # Backward compatibility for older state payloads.
            cur_out_dec = D(t.get("out_dec"))
            cur_in_dec = D(t.get("in_dec"))
            cur_out_amt = amt_floor(out_cur, cur_out_dec)
            cur_in_amt = amt_floor(in_cur, cur_in_dec)
        cur_out_u = amt_units_floor(cur_out_amt)
        cur_in_u = amt_units_floor(cur_in_amt)
        if cur_out_u <= 0 or cur_in_u <= 0:
            continue
        if out_take_u >= cur_out_u:
            t["out_amt"] = amt_zero(out_cur)
            t["in_amt"] = amt_zero(in_cur)
            continue

        rem_out_u = cur_out_u - out_take_u
        # Keep offer quality proportional in integer units.
        rem_in_u = (rem_out_u * cur_in_u + cur_out_u - 1) // cur_out_u

        if rem_out_u <= 0 or rem_in_u <= 0:
            t["out_amt"] = amt_zero(out_cur)
            t["in_amt"] = amt_zero(in_cur)
        else:
            t["out_amt"] = amt_from_units(out_cur, rem_out_u)
            t["in_amt"] = amt_from_units(in_cur, rem_in_u)


def _mtiers_by_id(mtiers: List[Dict[str, Any]]) -> Dict[str, Dict[str, Any]]:
    out: Dict[str, Dict[str, Any]] = {}
    for t in mtiers:
        sid = str(t.get("source_id") or "")
        if not sid:
            continue
        out[sid] = t
    return out


def _clone_mtiers(
    mtiers: List[Dict[str, Any]],
    *,
    in_cur: str,
    out_cur: str,
) -> List[Dict[str, Any]]:
    out: List[Dict[str, Any]] = []
