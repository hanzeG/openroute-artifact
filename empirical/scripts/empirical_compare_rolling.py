def fmt(v: Any, dp: Optional[int] = None) -> str:
    if v is None:
        return "None"
    if dp is None:
        dp = PRINT_DP
    try:
        d = v if isinstance(v, Decimal) else Decimal(str(v))
    except Exception:
        return str(v)
    if dp is None:
        return str(d)
    q = Decimal(1).scaleb(-int(dp))
    try:
        return str(d.quantize(q))
    except Exception:
        return str(d)


