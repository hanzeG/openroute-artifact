class PoolState:
    """
    Maintains rolling reserves for a pool in symmetric asset ordering.
    state.x/state.y are Amount-typed reserves for asset_a/asset_b.
    """
    def __init__(self, x_amt: Amount, y_amt: Amount):
        self.x = x_amt
        self.y = y_amt


def pool_key(amm_account: str, asset1: str, asset2: str) -> str:
    a, b = sorted([asset1, asset2])
    return f"{amm_account}|{a}|{b}"


def scoped_pool_key(base_key: str, ledger_index: int, state_mode: str) -> str:
    if state_mode == "ledger_local":
        return f"{int(ledger_index)}|{base_key}"
    return base_key


def clob_state_key(ledger_index: int, in_cur: str, out_cur: str, state_mode: str) -> Tuple[str, str, str]:
    if state_mode == "window_linked":
        return ("*", str(in_cur), str(out_cur))
    return (str(int(ledger_index)), str(in_cur), str(out_cur))


def orient_reserves(
    state: PoolState,
    in_cur: str,
    out_cur: str,
    pool_asset_a: str,
    pool_asset_b: str,
) -> Tuple[Amount, Amount, bool]:
    if in_cur == pool_asset_a and out_cur == pool_asset_b:
        return state.x, state.y, False
    if in_cur == pool_asset_b and out_cur == pool_asset_a:
        return state.y, state.x, True
    raise RuntimeError("orient_reserves: currencies do not match pool assets")


def write_back_reserves(
    state: PoolState,
    new_x: Amount,
    new_y: Amount,
    flipped: bool,
):
    if not flipped:
        state.x = new_x
        state.y = new_y
    else:
        state.x = new_y
        state.y = new_x


