def summarise_model_slices(
    quote: Dict[str, Any],
    *,
    in_cur: str,
    out_cur: str,
) -> Tuple[Decimal, Decimal, List[str], Amount, Amount]:
    """
    Returns:
      (amm_in_total_dec, amm_out_total_dec, path_steps_raw, amm_in_total_amt, amm_out_total_amt)
    """
    amm_in_amt = amt_zero(in_cur)
    amm_out_amt = amt_zero(out_cur)
    path: List[str] = []

    slices = quote.get("slices", []) or []
    for s in slices:
        if not isinstance(s, dict):
            continue
        in_take_amt = s.get("in_take")
        out_take_amt = s.get("out_take")
        if in_take_amt is None or out_take_amt is None:
            continue
        # Ignore zero-effect slices (router may emit AMM placeholders with 0/0).
        if amt_units_floor(in_take_amt) <= 0 or amt_units_floor(out_take_amt) <= 0:
            continue
        src = str(s.get("src") or s.get("kind") or "?")
        path.append(src)

        if "AMM" in src.upper():
            amm_in_amt = amm_in_amt + in_take_amt
            amm_out_amt = amm_out_amt + out_take_amt

    return (
        amount_to_decimal(amm_in_amt),
        amount_to_decimal(amm_out_amt),
        path,
        amm_in_amt,
        amm_out_amt,
    )


