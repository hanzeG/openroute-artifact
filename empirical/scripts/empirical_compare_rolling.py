    """Price as input-units per 1 output-unit, using Decimal converted amounts."""
    try:
        out_d = amount_to_decimal(out_amt)
        in_d = amount_to_decimal(in_amt)
        if out_d == 0:
            return None
        return in_d / out_d
    except Exception:
        return None


