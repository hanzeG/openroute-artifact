def enrich_slice_row_with_raw_amounts(row: Dict[str, Any]) -> None:
    """Dual-write canonical raw amount columns for per-slice outputs."""
    in_cur, out_cur = parse_direction_label(row.get("direction"))
    out_raw = raw_amount_from_cur_dec(out_cur, parse_decimal_opt(row.get("out_take")))
    in_raw = raw_amount_from_cur_dec(in_cur, parse_decimal_opt(row.get("in_take")))

