def _materialize_snapshot_book_offers(
    *,
    side_payload: Dict[str, Any],
    side_name: str,
    tx_hash: str,
    derived_owner_funds_by_offer_id: Optional[Dict[str, str]] = None,
) -> Tuple[List[Dict[str, Any]], str]:
    pre_ids = [str(x) for x in (side_payload.get("pre_overlay_offer_ids") or []) if x is not None and str(x) != ""]
    derived_owner_funds_by_offer_id = derived_owner_funds_by_offer_id or {}
    raw_offers = side_payload.get("offers") or []
    account_owner_funds_floor: Dict[str, Decimal] = {}
    for offer_row in raw_offers:
        if not isinstance(offer_row, dict):
            continue
        acct = str(offer_row.get("account") or "")
        raw_owner_funds = offer_row.get("owner_funds")
        if not acct or raw_owner_funds is None:
            continue
        try:
            owner_funds_dec = Decimal(str(raw_owner_funds))
        except Exception:
            continue
        if owner_funds_dec <= 0:
            continue
        prev = account_owner_funds_floor.get(acct)
        if prev is None or owner_funds_dec < prev:
            account_owner_funds_floor[acct] = owner_funds_dec
    enriched_rows: List[Dict[str, Any]] = []
    for offer_row in raw_offers:
        if not isinstance(offer_row, dict):
            enriched_rows.append(offer_row)
            continue
        patched = dict(offer_row)
        oid = str(offer_row.get("offer_id") or "")
        derived_owner_funds = derived_owner_funds_by_offer_id.get(oid)
        if derived_owner_funds is not None and oid and oid in pre_ids:
            patched["owner_funds"] = derived_owner_funds
        acct = str(patched.get("account") or "")
        shared_floor = account_owner_funds_floor.get(acct)
        if shared_floor is not None:
            patched["owner_funds"] = format(shared_floor, "f")
        enriched_rows.append(patched)
    mapped_rows = [r for r in (_snapshot_offer_to_book_offer(o) for o in enriched_rows) if r is not None]
    if pre_ids:
        # tx_prebook replay now emits the complete pre-tx executable book for the side.
        # `pre_overlay_offer_ids` is only provenance metadata for rows reconstructed from
        # tx metadata; it should not narrow compare-side execution back to a sparse subset.
        return mapped_rows, "all_offers_with_overlay_ids"
    return mapped_rows, "all_offers"


def _snapshot_offer_to_book_offer(offer_row: Dict[str, Any]) -> Optional[Dict[str, Any]]:
    if not isinstance(offer_row, dict):
        return None
    taker_gets = offer_row.get("taker_gets")
    taker_pays = offer_row.get("taker_pays")
    if taker_gets is None or taker_pays is None:
        return None
    out: Dict[str, Any] = {
        "TakerGets": taker_gets,
        "TakerPays": taker_pays,
    }
    if offer_row.get("offer_id") is not None:
        out["index"] = offer_row.get("offer_id")
    if offer_row.get("account") is not None:
        out["Account"] = offer_row.get("account")
    if offer_row.get("sequence") is not None:
        out["Sequence"] = offer_row.get("sequence")
    if offer_row.get("book_directory") is not None:
        out["BookDirectory"] = offer_row.get("book_directory")
    if offer_row.get("owner_funds") is not None:
        out["owner_funds"] = offer_row.get("owner_funds")
    if offer_row.get("taker_gets_funded") is not None:
        out["taker_gets_funded"] = offer_row.get("taker_gets_funded")
    if offer_row.get("taker_pays_funded") is not None:
        out["taker_pays_funded"] = offer_row.get("taker_pays_funded")
    return out


def _snapshot_rows_to_book_offers_by_id(rows: List[Dict[str, Any]]) -> Dict[str, Dict[str, Any]]:
    out: Dict[str, Dict[str, Any]] = {}
    for row in rows or []:
        mapped = _snapshot_offer_to_book_offer(row)
        if mapped is None:
            continue
        oid = mapped.get("index")
        if oid is None:
            continue
        out[str(oid)] = mapped
    return out


class TxPrebookSnapshotCursor:
    def __init__(self, path: str):
        self.path = str(path)
        self._f = open(self.path, "r", encoding="utf-8")
        self._line_no = 0
        self._pending: Optional[Dict[str, Any]] = None
        self._current_bundle: Optional[Dict[str, Any]] = None

    def close(self) -> None:
        try:
            self._f.close()
        except Exception:
            pass

    def _read_next_record(self) -> Optional[Dict[str, Any]]:
        while True:
            line = self._f.readline()
            if not line:
                return None
            self._line_no += 1
            s = line.strip()
            if not s:
                continue
            try:
                obj = json.loads(s)
            except Exception as e:
                raise RuntimeError(
                    f"tx_prebook snapshot parse failed at {self.path}:{self._line_no}: {type(e).__name__}: {e}"
                )
            if str(obj.get("scope") or "") != "tx_prebook_snapshot":
                continue
            try:
                li = int(obj.get("ledger_index"))
                txi = int(obj.get("transaction_index"))
            except Exception:
                continue
            txh = str(obj.get("transaction_hash") or "").upper()
            side = str(obj.get("side") or "")
            if side not in ("getsXRP", "getsrUSD"):
                continue
            used = obj.get("used_prebook_ledger")
            used_li = None
            if used is not None:
                try:
                    used_li = int(used)
                except Exception:
                    used_li = None
            offers = obj.get("offers")
            if not isinstance(offers, list):
                offers = []
            pre_overlay_offer_ids = obj.get("pre_overlay_offer_ids")
            if pre_overlay_offer_ids is None:
                pre_overlay_offer_ids = []
            if not isinstance(pre_overlay_offer_ids, list):
                raise RuntimeError(
                    "tx_prebook snapshot has invalid pre_overlay_offer_ids type "
                    f"(expected list, got {type(pre_overlay_offer_ids).__name__}) "
                    f"at {self.path}:{self._line_no}"
                )
            pre_overlay_offer_ids = [str(x) for x in pre_overlay_offer_ids if x is not None and str(x) != ""]
            return {
                "ledger_index": li,
                "transaction_index": txi,
                "transaction_hash": txh,
                "side": side,
                "used_prebook_ledger": used_li,
                "offers": offers,
                "pre_overlay_offer_ids": pre_overlay_offer_ids,
            }

    def _next_bundle(self) -> Optional[Dict[str, Any]]:
        rec = self._pending if self._pending is not None else self._read_next_record()
        self._pending = None
        if rec is None:
            return None

        li = int(rec["ledger_index"])
        txi = int(rec["transaction_index"])
        txh = str(rec["transaction_hash"])
        bundle = {
            "ledger_index": li,
            "transaction_index": txi,
            "transaction_hash": txh,
            "used_prebook_ledger": rec.get("used_prebook_ledger"),
            "sides": {
                str(rec["side"]): {
                    "offers": rec.get("offers") or [],
                    "pre_overlay_offer_ids": rec.get("pre_overlay_offer_ids") or [],
                }
            },
        }

        while True:
            nxt = self._read_next_record()
            if nxt is None:
                break
            same_tx = (
                int(nxt["ledger_index"]) == li
                and int(nxt["transaction_index"]) == txi
                and str(nxt["transaction_hash"]) == txh
            )
            if not same_tx:
                self._pending = nxt
                break

            side = str(nxt["side"])
            if side not in bundle["sides"]:
                bundle["sides"][side] = {
                    "offers": nxt.get("offers") or [],
                    "pre_overlay_offer_ids": nxt.get("pre_overlay_offer_ids") or [],
                }
            used_li = nxt.get("used_prebook_ledger")
            prev_used = bundle.get("used_prebook_ledger")
            if prev_used is None and used_li is not None:
                bundle["used_prebook_ledger"] = used_li
            elif prev_used is not None and used_li is not None and int(prev_used) != int(used_li):
                raise RuntimeError(
                    "tx_prebook snapshot used_prebook_ledger mismatch within same tx "
                    f"(ledger={li} tx_index={txi} hash={txh})"
                )
        return bundle

    def _advance_to(self, ledger_index: int, transaction_index: int) -> Optional[Dict[str, Any]]:
        target = (int(ledger_index), int(transaction_index))
        while True:
            cur = self._current_bundle
            if cur is None:
                cur = self._next_bundle()
                self._current_bundle = cur
            if cur is None:
                return None
            cur_key = (int(cur["ledger_index"]), int(cur["transaction_index"]))
            if cur_key < target:
                self._current_bundle = self._next_bundle()
                continue
            return cur

    def get_tx(self, ledger_index: int, transaction_index: int, tx_hash: str) -> Optional[Dict[str, Any]]:
        cur = self._advance_to(ledger_index, transaction_index)
        if cur is None:
            return None
        cur_key = (int(cur["ledger_index"]), int(cur["transaction_index"]))
        target = (int(ledger_index), int(transaction_index))
        if cur_key != target:
            return None
        cur_hash = str(cur.get("transaction_hash") or "").upper()
        want_hash = str(tx_hash or "").upper()
        if cur_hash != want_hash:
            raise RuntimeError(
                "tx_prebook snapshot hash mismatch at same (ledger,tx_index): "
                f"ledger={ledger_index} tx_index={transaction_index} "
                f"expected_hash={want_hash} got_hash={cur_hash}"
            )
        return cur


def _metadata_pre_state_field(node: Dict[str, Any], key: str) -> Any:
    prev = node.get("PreviousFields")
    if isinstance(prev, dict) and key in prev:
        return prev.get(key)
    fin = node.get("FinalFields")
    if isinstance(fin, dict) and key in fin:
        return fin.get(key)
    new = node.get("NewFields")
    if isinstance(new, dict) and key in new:
        return new.get(key)
    return None


def _metadata_path_accounts(result: Dict[str, Any]) -> Set[str]:
    paths_raw = result.get("Paths")
    if not isinstance(paths_raw, list):
        return set()
    out: Set[str] = set()
    for path in paths_raw:
        if not isinstance(path, list):
            continue
        for step in path:
            if not isinstance(step, dict):
                continue
            acct = step.get("account")
            if acct is not None and str(acct):
                out.add(str(acct))
    return out


def _amount_obj_to_decimal(raw: Any) -> Optional[Decimal]:
    if raw is None:
        return None
    try:
        if isinstance(raw, dict):
            value = raw.get("value")
            if value is None:
                return None
            return Decimal(str(value))
        return Decimal(str(raw))
    except Exception:
        return None


def _compute_amm_trade_from_metadata_result(
    *,
    result: Dict[str, Any],
    pool_accounts_hint: Set[str],
    in_cur: str,
    out_cur: str,
) -> Tuple[Optional[Decimal], Optional[Decimal], Optional[str]]:
    meta = result.get("meta") or {}
    affected = meta.get("AffectedNodes") or []

    pool_accounts: Set[str] = set()
    for wrapped in affected:
        if not isinstance(wrapped, dict):
            continue
        node = (
            wrapped.get("CreatedNode")
            or wrapped.get("ModifiedNode")
            or wrapped.get("DeletedNode")
        )
        if not isinstance(node, dict):
            continue
        prev = node.get("PreviousFields") or {}
        fin = node.get("FinalFields") or node.get("NewFields") or {}
        if str(node.get("LedgerEntryType") or "") == "AccountRoot":
            if ("AMMID" in prev) or ("AMMID" in fin):
                acct = fin.get("Account") or prev.get("Account")
                if acct:
                    pool_accounts.add(str(acct))

    path_accounts = _metadata_path_accounts(result)
    hinted_path_accounts = set(str(a) for a in path_accounts if str(a) in pool_accounts_hint)
    if hinted_path_accounts:
        pool_accounts.update(hinted_path_accounts)

    if not pool_accounts:
        return None, None, None

    deltas: Dict[str, Decimal] = {}
    for wrapped in affected:
        if not isinstance(wrapped, dict):
            continue
        node = (
            wrapped.get("CreatedNode")
            or wrapped.get("ModifiedNode")
            or wrapped.get("DeletedNode")
        )
        if not isinstance(node, dict):
            continue
        prev = node.get("PreviousFields") or {}
        fin = node.get("FinalFields") or node.get("NewFields") or {}
        le = str(node.get("LedgerEntryType") or "")

        if le == "AccountRoot":
            acct = fin.get("Account") or prev.get("Account")
            if acct not in pool_accounts:
                continue
            vb = _amount_obj_to_decimal(prev.get("Balance"))
            va = _amount_obj_to_decimal(fin.get("Balance"))
            if vb is None or va is None:
                continue
            deltas[XRP] = deltas.get(XRP, Decimal("0")) + (va - vb)
            continue

        if le != "RippleState":
            continue

        high_limit = fin.get("HighLimit") or prev.get("HighLimit") or {}
        low_limit = fin.get("LowLimit") or prev.get("LowLimit") or {}
        high = str(high_limit.get("issuer") or "")
        low = str(low_limit.get("issuer") or "")

        pool_side = None
        if high in pool_accounts:
            pool_side = "high"
        elif low in pool_accounts:
            pool_side = "low"
        if pool_side is None:
            continue

        bal_b = _amount_obj_to_decimal(prev.get("Balance"))
        bal_a = _amount_obj_to_decimal(fin.get("Balance"))
        bal_obj = fin.get("Balance") or prev.get("Balance") or {}
        ccy = str(bal_obj.get("currency") or "")
        if bal_b is None or bal_a is None or not ccy:
            continue

        if pool_side == "low":
            rb, ra = bal_b, bal_a
        else:
            rb, ra = -bal_b, -bal_a
        deltas[ccy] = deltas.get(ccy, Decimal("0")) + (ra - rb)

    d_in = deltas.get(in_cur)
    d_out = deltas.get(out_cur)
    in_take = max(d_in, Decimal("0")) if d_in is not None else None
    out_take = max(-d_out, Decimal("0")) if d_out is not None else None
    pool_account = sorted(pool_accounts)[0] if pool_accounts else None
    if in_take is None and out_take is None:
        return None, None, pool_account
    return out_take, in_take, pool_account


def _load_metadata_only_pair_amm_state_rows(
    *,
    metadata_ndjson: str,
    ledger_min: int,
    ledger_max: int,
    existing_tx_hashes: Set[str],
    pair_amm_accounts: Set[str],
) -> List[Dict[str, Any]]:
    out: List[Dict[str, Any]] = []
    if not pair_amm_accounts:
        return out

    with open(metadata_ndjson, "r", encoding="utf-8") as f:
        for line_no, line in enumerate(f, start=1):
            s = line.strip()
            if not s:
                continue
            obj = json.loads(s)
            if not isinstance(obj, dict):
                continue
            tx_hash = str(obj.get("tx_hash") or "").upper()
            if not tx_hash or tx_hash in existing_tx_hashes:
                continue
            result = obj.get("result")
            if not isinstance(result, dict):
                continue
            ledger_index = result.get("ledger_index")
            meta = result.get("meta") or {}
            tx_index = meta.get("TransactionIndex")
            if ledger_index is None or tx_index is None:
                continue
            try:
                ledger_index_i = int(ledger_index)
                tx_index_i = int(tx_index)
            except Exception:
                continue
            if ledger_index_i < ledger_min or ledger_index_i > ledger_max:
                continue
            tx_type = str(result.get("TransactionType") or "").upper()
            if tx_type != "PAYMENT":
                continue
            try:
                intent = build_intent_from_metadata_result(result)
            except Exception:
                continue
            if not _is_target_pair_direction(str(intent.in_cur), str(intent.out_cur)):
                continue
            path_accounts = _metadata_path_accounts(result)
            if not any(acct in pair_amm_accounts for acct in path_accounts):
                continue
            out_take, in_take, amm_account = _compute_amm_trade_from_metadata_result(
                result=result,
                pool_accounts_hint=pair_amm_accounts,
                in_cur=str(intent.in_cur),
                out_cur=str(intent.out_cur),
            )
            if out_take is None or in_take is None or in_take <= 0 or out_take <= 0:
                continue
            out.append(
                {
                    "transaction_hash": tx_hash,
                    "ledger_index": ledger_index_i,
                    "transaction_index": tx_index_i,
                    "amm_account": str(amm_account or next(iter(pair_amm_accounts))),
                    "asset_in_currency": str(intent.in_cur),
                    "asset_out_currency": str(intent.out_cur),
                    "asset_in_value": str(in_take),
                    "asset_out_value": str(out_take),
                    "metadata_only_amm_state": 1,
                    "force_state_only": 1,
                }
            )
    return out


def _metadata_pre_offer_from_node(body: Dict[str, Any]) -> Optional[Dict[str, Any]]:
    final_fields = body.get("FinalFields")
    prev_fields = body.get("PreviousFields")
    if not isinstance(final_fields, dict) or not isinstance(prev_fields, dict):
        return None
    pre_offer = dict(final_fields)
    pre_offer.update(prev_fields)
    if pre_offer.get("TakerGets") is None or pre_offer.get("TakerPays") is None:
        return None
    if pre_offer.get("Account") is None and body.get("Account") is not None:
        pre_offer["Account"] = body.get("Account")
    return pre_offer


def _metadata_pre_xrp_owner_funds(
    *,
    account: str,
    account_root_pre_by_account: Dict[str, Tuple[int, int]],
) -> Optional[str]:
    state = account_root_pre_by_account.get(account)
    if state is None:
        return None
    bal_drops, owner_count = state
    reserve_drops = (
        XRPL_OWNER_FUNDS_RESERVE_BASE_DROPS
        + max(int(owner_count), 0) * XRPL_OWNER_FUNDS_RESERVE_INC_DROPS
    )
    spendable_drops = bal_drops - reserve_drops
    if spendable_drops <= 0:
        return None
    return str(spendable_drops)


def _metadata_pre_iou_owner_funds(
    *,
    account: str,
    currency: str,
    issuer: str,
    ripple_pre_by_key: Dict[Tuple[str, str, str], Decimal],
) -> Optional[str]:
    bal = ripple_pre_by_key.get((account, currency, issuer))
    if bal is None or bal <= 0:
        return None
    return format(bal, "f")


def _extract_overlay_owner_funds_from_metadata_result(result: Dict[str, Any]) -> Dict[str, str]:
    out: Dict[str, str] = {}
    meta = result.get("meta")
    if not isinstance(meta, dict):
        return out
    affected = meta.get("AffectedNodes")
    if not isinstance(affected, list):
        return out

    account_root_pre_by_account: Dict[str, Tuple[int, int]] = {}
    ripple_pre_by_key: Dict[Tuple[str, str, str], Decimal] = {}

    for wrapped in affected:
        if not isinstance(wrapped, dict):
            continue
        node_kind = None
        body = None
        for candidate in ("ModifiedNode", "DeletedNode", "CreatedNode"):
            maybe = wrapped.get(candidate)
            if isinstance(maybe, dict):
                node_kind = candidate
                body = maybe
                break
        if body is None or node_kind is None:
            continue
        entry_type = str(body.get("LedgerEntryType") or "")
        if entry_type == "AccountRoot":
            account = str(_metadata_pre_state_field(body, "Account") or "")
            bal_raw = _metadata_pre_state_field(body, "Balance")
            owner_count_raw = _metadata_pre_state_field(body, "OwnerCount")
            if not account or bal_raw is None:
                continue
            try:
                bal_drops = int(str(bal_raw))
                owner_count = int(str(owner_count_raw)) if owner_count_raw is not None else 0
            except Exception:
                continue
            account_root_pre_by_account[account] = (bal_drops, owner_count)
            continue
        if entry_type == "RippleState":
            bal_raw = _metadata_pre_state_field(body, "Balance")
            low_limit = _metadata_pre_state_field(body, "LowLimit")
            high_limit = _metadata_pre_state_field(body, "HighLimit")
            if (
                not isinstance(bal_raw, dict)
                or not isinstance(low_limit, dict)
                or not isinstance(high_limit, dict)
            ):
                continue
            currency = str(
                bal_raw.get("currency")
                or low_limit.get("currency")
                or high_limit.get("currency")
                or ""
            )
            low_acc = str(low_limit.get("issuer") or "")
            high_acc = str(high_limit.get("issuer") or "")
            if not currency or not low_acc or not high_acc:
                continue
            try:
                bal_val = Decimal(str(bal_raw.get("value")))
            except Exception:
                continue
            low_bal = bal_val
            high_bal = -bal_val
            if low_bal > 0:
                ripple_pre_by_key[(low_acc, currency, high_acc)] = low_bal
            if high_bal > 0:
                ripple_pre_by_key[(high_acc, currency, low_acc)] = high_bal
            continue

    for wrapped in affected:
        if not isinstance(wrapped, dict):
            continue
        node_kind = None
        body = None
        for candidate in ("ModifiedNode", "DeletedNode"):
            maybe = wrapped.get(candidate)
            if isinstance(maybe, dict):
                node_kind = candidate
                body = maybe
                break
        if body is None or node_kind is None:
            continue
        if str(body.get("LedgerEntryType") or "") != "Offer":
            continue
        offer_id = str(body.get("LedgerIndex") or "")
        if not offer_id:
            continue
        pre_offer = _metadata_pre_offer_from_node(body)
        if not isinstance(pre_offer, dict):
            continue
        account = str(pre_offer.get("Account") or "")
        pre_gets_raw = pre_offer.get("TakerGets")
        if not account or pre_gets_raw is None:
            continue
        owner_funds: Optional[str]
        if isinstance(pre_gets_raw, str):
            owner_funds = _metadata_pre_xrp_owner_funds(
                account=account,
                account_root_pre_by_account=account_root_pre_by_account,
            )
        elif isinstance(pre_gets_raw, dict):
            currency = str(pre_gets_raw.get("currency") or "")
            issuer = str(pre_gets_raw.get("issuer") or "")
            if not currency or not issuer:
                continue
            owner_funds = _metadata_pre_iou_owner_funds(
                account=account,
                currency=currency,
                issuer=issuer,
                ripple_pre_by_key=ripple_pre_by_key,
            )
        else:
            owner_funds = None
        if owner_funds is None:
            continue
        out[offer_id] = owner_funds
    return out


def load_tx_overlay_owner_funds_from_metadata(
    *,
    metadata_ndjson: str,
    target_tx_hashes: Set[str],
) -> Dict[str, Dict[str, str]]:
    out: Dict[str, Dict[str, str]] = {}
    with open(metadata_ndjson, "r", encoding="utf-8") as f:
        for line_no, line in enumerate(f, start=1):
            s = line.strip()
            if not s:
                continue
            try:
                obj = json.loads(s)
            except Exception as e:
                raise RuntimeError(
                    f"metadata parse failed at {metadata_ndjson}:{line_no}: {type(e).__name__}: {e}"
                ) from e
            if not isinstance(obj, dict):
                continue
            result = obj.get("result")
            if not isinstance(result, dict):
                continue
            txh = str(obj.get("tx_hash") or result.get("hash") or "").upper()
            if not txh or txh not in target_tx_hashes:
                continue
            out[txh] = _extract_overlay_owner_funds_from_metadata_result(result)
    return out


def build_tiers_from_offers(
    offers: List[Dict[str, Any]],
    in_cur: str,
    out_cur: str,
    *,
    exclude_account: Optional[str] = None,
    allow_empty: bool = False,
) -> List[Tuple[Quality, Any, Any, Any, str]]:
    segs = build_clob_segments_from_offers(
        offers,
        in_cur=in_cur,
        out_cur=out_cur,
        iou_in_transfer_rate=IOU_IN_TRANSFER_RATE,
        exclude_account=exclude_account,
    )
    if not segs:
        if allow_empty:
            return []
        raise RuntimeError("No direction-aligned offers found for this IN/OUT.")
    tiers: List[Tuple[Quality, Any, Any, Any, str]] = []
    for seg in segs:
        tiers.append(
            (
                seg.quality,
                seg.out_max,
                seg.in_at_out_max,
                None,
                str(seg.source_id or ""),
            )
        )
    return tiers


def best_offer_quality_for_account(
    offers: List[Dict[str, Any]],
    *,
    account: Optional[str],
    exclude_sequence: Optional[int] = None,
    in_cur: str,
    out_cur: str,
) -> Optional[Quality]:
    acct = str(account or "")
    if not acct:
        return None
    account_rows = []
    for o in offers or []:
        if str(o.get("Account") or "") != acct:
            continue
        seq = o.get("Sequence")
        if exclude_sequence is not None and seq is not None and int(seq) == int(exclude_sequence):
            continue
        account_rows.append(o)
    if not account_rows:
        return None
    segs = build_clob_segments_from_offers(
        account_rows,
        in_cur=in_cur,
        out_cur=out_cur,
        iou_in_transfer_rate=IOU_IN_TRANSFER_RATE,
    )
    if not segs:
        return None
    best = max(segs, key=lambda seg: seg.quality.rate)
    return best.quality


