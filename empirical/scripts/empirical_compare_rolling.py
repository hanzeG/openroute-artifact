def direction_label(in_cur: str, out_cur: str) -> str:
    return f"{pretty_cur(in_cur)}->{pretty_cur(out_cur)}"


def parse_direction_label(dir_lbl: Any) -> Tuple[Optional[str], Optional[str]]:
    """Parse direction label back to canonical currencies when possible."""
    if dir_lbl is None:
        return None, None
    s = str(dir_lbl)
    if "->" not in s:
        return None, None
    left, right = [x.strip() for x in s.split("->", 1)]

    def _canon(c: str) -> str:
        if c == "rUSD":
            return RUSD_HEX
        return c

    return _canon(left), _canon(right)


def parse_decimal_opt(x: Any) -> Optional[Decimal]:
    if x is None:
        return None
    sx = str(x).strip()
    if sx == "" or sx.lower() in {"none", "nan"}:
        return None
    try:
        return Decimal(sx)
    except Exception:
        return None


def raw_amount_from_cur_dec(cur: Optional[str], dec_val: Optional[Decimal]) -> Dict[str, Any]:
    """Convert (currency, Decimal amount) into canonical raw amount fields."""
    out = {
        "kind": None,
        "drops": None,
        "mantissa": None,
        "exponent": None,
        "ok": None,
    }
    if cur is None:
        return out
    if dec_val is None:
        out["kind"] = "XRP" if is_xrp(cur) else "IOU"
        return out

    if is_xrp(cur):
        out["kind"] = "XRP"
        try:
            n = dec_val / XRP_QUANTUM
            if n == n.to_integral_value():
                out["drops"] = int(n)
                out["ok"] = True
            else:
                out["ok"] = False
        except Exception:
            out["ok"] = False
        return out

    out["kind"] = "IOU"
    try:
        a = IOUAmount.from_decimal(dec_val)
        out["mantissa"] = int(a.mantissa)
        out["exponent"] = int(a.exponent)
        out["ok"] = True
    except Exception:
        out["ok"] = False
    return out


def raw_amount_to_obj(kind: Any, drops: Any, mantissa: Any, exponent: Any) -> Optional[Amount]:
    if kind == "XRP":
        if drops is None:
            return None
        try:
            return XRPAmount(int(drops))
        except Exception:
            return None
    if kind == "IOU":
        if mantissa is None or exponent is None:
            return None
        try:
            return IOUAmount.from_components(int(mantissa), int(exponent))
        except Exception:
            return None
    return None


def _attach_raw_amount_fields(
    row: Dict[str, Any],
    *,
    prefix: str,
    cur: Optional[str],
    dec_val: Optional[Decimal],
) -> None:
    raw = raw_amount_from_cur_dec(cur, dec_val)
    row[f"{prefix}_kind"] = raw["kind"]
    row[f"{prefix}_drops"] = raw["drops"]
    row[f"{prefix}_mantissa"] = raw["mantissa"]
    row[f"{prefix}_exponent"] = raw["exponent"]
    row[f"{prefix}_raw_ok"] = raw["ok"]


def _attach_raw_quality_fields(
    row: Dict[str, Any],
    *,
    q_prefix: str,
    out_prefix: str,
    in_prefix: str,
) -> None:
    out_amt = raw_amount_to_obj(
        row.get(f"{out_prefix}_kind"),
        row.get(f"{out_prefix}_drops"),
        row.get(f"{out_prefix}_mantissa"),
        row.get(f"{out_prefix}_exponent"),
    )
    in_amt = raw_amount_to_obj(
        row.get(f"{in_prefix}_kind"),
        row.get(f"{in_prefix}_drops"),
        row.get(f"{in_prefix}_mantissa"),
        row.get(f"{in_prefix}_exponent"),
    )
    if out_amt is None or in_amt is None or out_amt.is_zero() or in_amt.is_zero():
        row[f"{q_prefix}_mantissa"] = None
        row[f"{q_prefix}_exponent"] = None
        row[f"{q_prefix}_raw_ok"] = None
        return
    try:
        q = Quality.from_amounts(out_amt, in_amt)
        row[f"{q_prefix}_mantissa"] = int(q.rate.mantissa)
        row[f"{q_prefix}_exponent"] = int(q.rate.exponent)
        row[f"{q_prefix}_raw_ok"] = True
    except Exception:
        row[f"{q_prefix}_mantissa"] = None
        row[f"{q_prefix}_exponent"] = None
        row[f"{q_prefix}_raw_ok"] = False


