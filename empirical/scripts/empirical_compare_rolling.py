def amt_floor(cur: str, v: Decimal):
    if is_xrp(cur):
        drops = int((v / XRP_QUANTUM).to_integral_value(rounding=ROUND_FLOOR))
        return XRPAmount(value=drops)
    units = int((v / IOU_QUANTUM).to_integral_value(rounding=ROUND_FLOOR))
    return IOUAmount.from_components(units, -15)


def amt_zero(cur: str) -> Amount:
    return XRPAmount(0) if is_xrp(cur) else IOUAmount.zero()


def amt_units_floor(a: Amount) -> int:
    if isinstance(a, XRPAmount):
        return a.value if a.value > 0 else 0
    m = a.mantissa
    e = a.exponent
    if m <= 0:
        return 0
    k = e + 15
    if k >= 0:
        return m * (10 ** k)
    div = 10 ** (-k)
    return m // div


def amt_from_units(cur: str, units: int) -> Amount:
    if units <= 0:
        return amt_zero(cur)
    if is_xrp(cur):
        return XRPAmount(int(units))
    return IOUAmount.from_components(int(units), -15)


